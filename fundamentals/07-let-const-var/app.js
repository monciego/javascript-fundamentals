// let vs. const vs. var

// using var
var age = 21;
age = 22;

// using let
let firstName = "jericho";
firstName = "Jericho";

// using const or constant -> can't reassign a value
// always declare a value in const

const lastName = "bantiquete";
// lastName = "Bantiquete"; // TypeError: Assignment to constant variable.

// console.logs
console.log(age);
console.log(firstName);
console.log(lastName);

