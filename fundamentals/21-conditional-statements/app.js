// conditional statements
// >, <, >=, <=, ==, ===, !=, !===

// checks if it is true or false

if (true) {
  console.log("Hello World");
}

const value = 2 < 1;
if (value) {
  console.log(value); // it won't print
}

const num = 21;

if (num >= 18) {
  console.log("the number is greater than or equals to 18"); // this will print of the condition is true
} else {
  console.log("the number is less than 18"); // this will print if the condition is false
}
